/** @param {NavLang} lang @param {string} key */
export function mpT(lang, key) {
  return MP_STRINGS[lang]?.[key] ?? MP_STRINGS.it[key] ?? key;
}

/** @param {unknown} v */
export function normalizeNavLang(v) {
  return v === "en" || v === "fr" || v === "it" ? v : "it";
}
