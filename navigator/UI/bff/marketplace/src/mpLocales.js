/** @typedef {"it"|"en"|"fr"} NavLang */

const LS_KEY = "mu_nav_lang";

/** @returns {NavLang} */
export function readStoredNavLang() {
  try {
    const v = localStorage.getItem(LS_KEY);
    if (v === "en" || v === "fr" || v === "it") return v;
  } catch {
    /* ignore */
  }
  return "it";
}

/** @param {NavLang} l */
export function writeStoredNavLang(l) {
  try {
    localStorage.setItem(LS_KEY, l);
  } catch {
    /* ignore */
  }
}

